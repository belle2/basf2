double ECLDigiModule::ShaperDSP(double Ti)
{
  double svp = 0;
  double tr1 = Ti * 0.881944444;
  double s[12] = {0, 27.7221, 0.2, 0.6483, 0.4017, 0.3741, 0.8494, 0.00144547, 4.7071, 0.8156, 0.5556, 0.2752};


  double tr = tr1 - s[2];
  double tr2 = tr + .2;
  double tr3 = tr - .2;
  if (tr2 > 0.) {

    svp = (Sv123(tr , s[4], s[5], s[9], s[10], s[3], s[6]) * (1 - s[11])
           + s[11] * .5 * (Sv123(tr2, s[4], s[5], s[9], s[10], s[3], s[6])
                           + Sv123(tr3, s[4], s[5], s[9], s[10], s[3], s[6])));
    double x = tr / s[4];
    x = tr / s[4];


    svp = s[1] * (svp - s[7] * (exp(-tr / s[8]) *
                                (1 - exp(-x) * (1 + x + x * x / 2 + x * x * x / 6 + x * x * x * x / 24 + x * x * x * x * x / 120))));
  } else svp = 0 ;
  return svp;

}


double  ECLDigiModule::Sv123(double t, double t01, double tb1, double t02, double tb2, double td1, double ts1)
{

  double sv123 = 0.;
  double  dks0, dks1, dksm,
  dw0, dw1, dwp, dwm, das1, dac1, das0, dac0, dzna, dksm2, ds, dd,
  dcs0, dsn0, dzn0, td, ts, dr,
  dcs0s, dsn0s, dcs0d, dsn0d, dcs1s, dsn1s, dcs1d, dsn1d;


  if (t < 0.) return 0.;

  dr = (ts1 - td1) / td1;
  if (abs(dr) >= 0.0000001) {
    td = td1;
    ts = ts1;
  } else {
    td = td1;
    if (ts1 > td1) {
      ts = td1 * 1.00001;
    } else {
      ts = td1 * 0.99999;
    }
  }

  dr = ((t01 - t02) * (t01 - t02) + (tb1 - tb2) * (tb1 - tb2)) / ((t01) * (t01) + (tb1) * (tb1));
  dks0 = 1.0 / t01;
  dks1 = 1.0 / t02;

  if (dr < 0.0000000001) {

    if (dks0 > dks1) {
      dks0 = dks1 * 1.00001;
    } else {
      dks0 = dks1 * 0.99999;
    }
  }

  if (t < 0.) return 0;



  dksm = dks1 - dks0;

  ds = 1. / ts;
  dd = 1. / td;

  dw0 = 1. / tb1;
  dw1 = 1. / tb2;
  dwp = dw0 + dw1;
  dwm = dw1 - dw0;

  dksm2 = dksm * dksm;

  dzna = (dksm2 + dwm * dwm) * (dksm2 + dwp * dwp);


  das0 = dw1 * (dksm2 + dwp * dwm);
  dac0 = -2 * dksm * dw0 * dw1;
  das1 = dw0 * (dksm2 - dwp * dwm);
  dac1 = -dac0;





  dsn0 = (ds - dks0);
  dcs0 = -dw0;
  dzn0 = dcs0 * dcs0 + dsn0 * dsn0;

  dsn0s = (dsn0 * das0 - dcs0 * dac0) / dzn0;
  dcs0s = (dcs0 * das0 + dsn0 * dac0) / dzn0;

  dsn0 = (ds - dks1);
  dcs0 = -dw1;
  dzn0 = dcs0 * dcs0 + dsn0 * dsn0;

  dsn1s = (dsn0 * das1 - dcs0 * dac1) / dzn0;
  dcs1s = (dcs0 * das1 + dsn0 * dac1) / dzn0;


  dsn0 = (dd - dks0);
  dcs0 = -dw0;
  dzn0 = dcs0 * dcs0 + dsn0 * dsn0;

  dsn0d = (dsn0 * das0 - dcs0 * dac0) / dzn0;
  dcs0d = (dcs0 * das0 + dsn0 * dac0) / dzn0;

  dsn0 = (dd - dks1);
  dcs0 = -dw1;
  dzn0 = dcs0 * dcs0 + dsn0 * dsn0;

  dsn1d = (dsn0 * das1 - dcs0 * dac1) / dzn0;
  dcs1d = (dcs0 * das1 + dsn0 * dac1) / dzn0;

  dr = (ts - td) / td;




  sv123 = ((((dsn0s - dsn0d) * sin(dw0 * t)
             + (dcs0s - dcs0d) * cos(dw0 * t)) * exp(-t * dks0)
            - (dcs0s + dcs1s) * exp(-t * ds) + (dcs0d + dcs1d) * exp(-t * dd)
            + ((dsn1s - dsn1d) * sin(dw1 * t)
               + (dcs1s - dcs1d) * cos(dw1 * t)) * exp(-t * dks1)) / dzna / (ts - td));

  return sv123;


}


void ECLDigiModule::shapeFitter(short int *id, int *f, int *f1, int *fg41, int *fg43, int *fg31, int *fg32, int *fg33, int *y, int *ttrig, int *n16, int *ch, int *lar, int *ltr, int *lq)
{



  /*
   cout<<"Fit Array: \n";
   for(int testi=0;testi<1;testi++) {
    for(int testj=0;testj<31;testj++){
    cout<<*(y+testi *16+testj)<<" ";
   }cout<<endl;}cout<<endl<<endl;;

   cout<<"fg41: " <<endl;
   for(int testi=0;testi<24;testi++) {
    for(int testj=0;testj<16;testj++){
    cout<<*(fg41+testi *16+testj)<<" ";
   }cout<<endl;}cout<<endl<<endl;;

   cout<<"fg43: " <<endl;
   for(int testi=0;testi<24;testi++) {
    for(int testj=0;testj<16;testj++){
    cout<<*(fg43+testi *16+testj)<<" ";
   }cout<<endl;}cout<<endl<<endl;;

   cout<<"id: " <<endl;
   for(int testi=0;testi<16;testi++) {
    for(int testj=0;testj<16;testj++){
    cout<<*(id+testi *16+testj)<<" ";
   }cout<<endl;}cout<<endl<<endl;
    */
  static long long int k_np[16] = {
    65536,
    32768,
    21845,
    16384,
    13107,
    10923,
    9362,
    8192,
    7282,
    6554,
    5958,
    5461,
    5041,
    4681,
    4369,
    4096
  };
//  long long int b32=4294967295;
  int A0  = (int) * (id + 128 + *ch) - 128;
  int Askip  = (int) * (id + 192 + *ch) - 128;
  //int Askip  = -127-128;
  int Ahard  = (int) * (id + 64 + *ch);
  int k_a = (int) * ((unsigned char*)id + 26);
  int k_b = (int) * ((unsigned char*)id + 27);
  int k_c = (int) * ((unsigned char*)id + 28);
  int k_16 = (int) * ((unsigned char*)id + 29);

  int k1_chi = (int) * ((unsigned char*)id + 32);
  int k2_chi = (int) * ((unsigned char*)id + 33);
  int chi_thres = (int) * (id + 15);


  int s1, s2;


  long long int z0;
  int it, it0;
//  long long d_it;
  int it_h, it_l;
  long long A1, B1, A2, C1, ch1, ch2, B2, B3, B5 ;
  int low_ampl, i, T, iter;



  if (k_16 + *n16 != 16) {
    printf("disagreement in number of the points %d %d \n", k_16, *n16);
  }


  int validity_code = 0;
  for (i = 0, z0 = 0; i < 16; i++)
    z0 += y[i];

  //initial time index

  it0 = 48 + ((23 - *ttrig) << 2);




  //limits

  it_h = 191;
  it_l = 0;
  it = it0;

  //first approximation without time correction

  //  int it00=23-it0;

  A2 = (*(fg41 + *ttrig * 16));


  A2 = (A2 * z0);


  for (i = 1; i < 16; i++) {
    s1 = (*(fg41 + *ttrig * 16 + i));
    B3 = y[15+i];
    B3 = s1 * B3;
    A2 += B3;

  }
  A2 += (1 << (k_a - 1));
  A2 >>= k_a;
  T = it0 << 4;


  //too large amplitude
  if (A2 > 262143) {
    A1 = A2 >> 3;
    validity_code = 1;

    goto ou;
  }


  low_ampl = 0;
  if (A2 >= A0) {

    for (iter = 0, it = it0; iter < 3;) {
      iter++;
      s1 = (*(fg31 + it * 16));
      s2 = (*(fg32 + it * 16));
      A1 = (s1 * z0);
      B1 = (s2 * z0);



      for (i = 1; i < 16; i++) {
        s1 = (*(fg31 + i + it * 16));
        s2 = (*(fg32 + i + it * 16));
        B5 = y[15+i];
        B5 = s1 * B5;
        A1 += B5;

        B3 = y[15+i];
        B3 = s2 * B3;
        B1 += B3;
      }
      A1 += (1 << (k_a - 1));
      A1 = A1 >> k_a;


      if (A1 > 262143)
        goto ou;
      if (A1 < A0) {

        low_ampl = 1;
        it = it0;

        goto lam;
      }

      if (iter != 3) {

        B2 = B1 >> (k_b - 9);
        B1 = B2 >> 9;

        B2 += (A1 << 9);


        B3 = (B2 / A1);

        it += ((B3 + 1) >> 1) - 256;
        it = it > it_h ? it_h : it;
        it = it < it_l ? it_l : it;

      } else {
        B2 = B1 >> (k_b - 13);
        B5 = B1 >> (k_b - 9);

        B1 = B2 >> 13;
        B2 += (A1 << 13);
        B3 = (B2 / A1);

        T = ((it) << 4) + ((B3 + 1) >> 1) - 4096;

        B1 = B5 >> 9;
        B5 += (A1 << 9);
        B3 = (B5 / A1);
        it += ((B3 + 1) >> 1) - 256;
        it = it > it_h ? it_h : it;
        it = it < it_l ? it_l : it;

        T = T > 3071 ?  3071 : T;

        T = T < 0 ? 0 : T;

        C1 = (*(fg33 + it * 16) * z0);
        for (i = 1; i < 16; i++)
          C1 += *(fg33 + i + it * 16) * y[15+i];
        C1 += (1 << (k_c - 1));
        C1 >>= k_c;

      }

    }
  } else
    low_ampl = 1;

  if (low_ampl == 1) {

lam:
    A1 = A2;
    validity_code = 0;
    B1 = 0;
    C1 = (*(fg43 + *ttrig * 16) * z0);
    for (i = 1; i < 16; i++) {
      B5 = y[15+i];
      C1 += *(fg43 + i + *ttrig * 16) * B5;
    }
    C1 += (1 << (k_c - 1));
    C1 >>= k_c;
  }
  ch2 = (A1* *(f + it * 16) + B1* *(f1 + it * 16)) >> k1_chi;
  ch2 += C1;
  ch2 = z0 - *n16 * ch2;
  ch1 = ((ch2) * (ch2));
  ch1 = ch1 * k_np[*n16-1];
  ch1 = ch1 >> 16;
  for (i = 1; i < 16; i++) {
    ch2 = A1 * (*(f + i + it * 16)) + B1 * (*(f1 + i + it * 16));
    ch2 >>= k1_chi;
    ch2 = (y[i+15] - ch2 - C1);

    ch1 = ch1 + ch2 * ch2;

  }
  B2 = (A1 >> 1) * (A1 >> 1);
  B2 >>= (k2_chi - 2);
  B2 += chi_thres;
  if (ch1 > B2)validity_code = 3;
ou:

  *lar = A1;
  *ltr = T;

  if (A1 < Askip)validity_code = validity_code + 8;

  int ss = (y[20] + y[21]);

  if (ss <= Ahard)validity_code = validity_code + 4;


  *lq = validity_code;


  return ;
}


