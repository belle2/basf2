void ECLDigiModule::event()
{
  cout.setf(ios::fixed);
  cout.precision(6);

  m_timeCPU = clock() * Unit::us;
  B2INFO("ECLDigiModule m_timeCPU Hit initialize " << m_timeCPU << " us ;" << m_timeCPU / Unit::ms << " ms.");
  //Input Array
  StoreArray<HitECL>  eclArray(m_eclHitCollectionName);
  if (!eclArray) {
    B2ERROR("Can not find ECLDigiHits" << m_eclHitCollectionName << ".");
  }

  int hitNum = eclArray->GetEntriesFast();

  double energyFit[8736] = {0}; //fit output : Amplitude
  double tFit[8736] = {0};    //fit output : T_ave
  int qualityFit[8736] = {0};    //fit output : T_ave
  double HitEnergy[8736][31];
  double E_tmp[8736] = {0};
  double test_A[31] = {0};
//  double AdcPedestal=3000.;
  double AdcNoise[8736][31];
  double genNoise[8736][31];



  for (int i = 0; i < 8736; i++) {for (int j = 0; j < 31; j++) {HitEnergy[i][j] = 0; AdcNoise[i][j] = 0; genNoise[i][j] = 0;}}
  int DeltaT = (int)(24. - m_random->Uniform(0, 24));
  for (int ii = 0; ii < hitNum; ii++) {

    HitECL* aECLHit = eclArray[ii];
    // Hit geom. info
//    int hitEventId       =  aECLHit->getEventId();

    int hitCellId       =  aECLHit->getCellId();
    double hitE         =  aECLHit->getEnergyDep() / Unit::GeV;
    double hitTimeAve       =  aECLHit->getTimeAve()   / Unit::us;
    double sampleTime ;



    E_tmp[hitCellId] = hitE + E_tmp[hitCellId];//for summation deposit energy; do fit if this summation > 0.1 MeV
    cout << "HitEnergy ADC  :" << hitCellId << " " << hitE << " " << hitTimeAve << endl;

    //Noise generation
    for (int iCal = 0; iCal < 31; iCal++) {
      genNoise[hitCellId][iCal] = m_random->Gaus(0, 1);
    }

    for (int T_clock = 0; T_clock < 31; T_clock++) {
      for (int iCal = 0; iCal < 31; iCal++) {
        AdcNoise[hitCellId][T_clock] = m_vmat[T_clock][iCal] * genNoise[hitCellId][iCal] + AdcNoise[hitCellId][T_clock];
      }
    }

    for (int T_clock = 0; T_clock < 31; T_clock++) {
      double timeInt =  DeltaT * 12. / 508.; //us
      sampleTime = (24. * 12. / 508.)  * (T_clock - 15) - hitTimeAve - timeInt ;
      test_A[T_clock] = ShaperDSP(sampleTime);

      HitEnergy[hitCellId][T_clock] = test_A[T_clock]  * hitE  +  HitEnergy[hitCellId][T_clock];
    }//for T_clock 31 clock

  } //end loop over eclHitArray ii



  for (int iECLCell = 0; iECLCell < 8736; iECLCell++) {
    if (E_tmp[iECLCell] > 0.0001) {


      /*
          cout<<"Gaussian Noise \n";
          for(int j=14;j<31;j++)cout<<genNoise[j]<<" ";
          cout<<endl;
      */
//    cout<<"HitEnergy (ADC) : "<< E_tmp[iECLCell]*20000<<" ; Energy (GeV): "<< E_tmp[iECLCell]     <<endl;
//    for(int j=14;j<31;j++)cout<<HitEnergy[iECLCell][j]*20000 <<" ";
//    cout<<endl;;

      for (int  T_clock = 0; T_clock < 31; T_clock++) {
        FitA[T_clock] = (int)(HitEnergy[iECLCell][T_clock] * 20000 + 3000 + AdcNoise[iECLCell][T_clock] * 20) ;
      }//for T_clock 31 clock

      /*
         for(int j=14;j<31;j++)cout<<test_A[j]<<" ";
          cout<<endl;
         for(int j=14;j<31;j++)cout<<HitEnergy[iECLCell][j]<<" ";
          cout<<endl;

      */
      m_n16 = 16;
      m_ttrig = DeltaT;
      shapeFitter(&(m_id[0][0]), &(m_f[0][0]), &(m_f1[0][0]), &(m_fg41[0][0]), &(m_fg43[0][0]), &(m_fg31[0][0]), &(m_fg32[0][0]), &(m_fg33[0][0]), &(FitA[0]), &m_ttrig,  &m_n16, &m_ch, &m_lar, &m_ltr, &m_lq);

      energyFit[iECLCell] = m_lar; //fit output : Amplitude
      tFit[iECLCell] = m_ttrig;    //fit output : T_ave
      qualityFit[iECLCell] = m_lq;    //fit output : T_ave

      cout << "Fit result Cell ID: " << iECLCell << " energy: " << energyFit[iECLCell] << " t: " << tFit[iECLCell] << " quality: " << qualityFit[iECLCell] << " DeltaT " << DeltaT << endl;

      StoreArray<DigiECL> eclDigiArray(m_eclDigiCollectionName);
      m_hitNum = eclDigiArray->GetLast() + 1;
      new(eclDigiArray->AddrAt(m_hitNum)) DigiECL();
      eclDigiArray[m_hitNum]->setEventId(m_nEvent);
      eclDigiArray[m_hitNum]->setCellId(iECLCell);
      eclDigiArray[m_hitNum]->setAmp(energyFit[iECLCell]);
      eclDigiArray[m_hitNum]->setTimeFit(tFit[iECLCell]);
      eclDigiArray[m_hitNum]->setQuality(qualityFit[iECLCell]);

      StoreArray<DspECL> eclDspArray(m_eclDspCollectionName);
      m_hitNum = eclDspArray->GetLast() + 1;
      new(eclDspArray->AddrAt(m_hitNum)) DspECL();
      eclDspArray[m_hitNum]->setEventId(m_nEvent);
      eclDspArray[m_hitNum]->setCellId(iECLCell);
      eclDspArray[m_hitNum]->setDspA(FitA);

    }//if Energy > 0.1 MeV
  } //store  each crystal hit


  cout << "m_nEvent++ : " << m_nEvent++    << endl;
  m_nEvent++;
  m_timeCPU = clock() * Unit::us;
  B2INFO("ECLDigiModule finished. Time per event: " << m_timeCPU  / Unit::ms << " ms.");

}

