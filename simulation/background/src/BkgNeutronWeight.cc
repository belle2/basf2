  void BkgNeutronWeight::doSpline()
  {
    // converted from fortran in Bevington's Data Analysis ... ugh!
    m_n = m_x.size();
    int n = m_n;
    int i = 0;

    m_d[0] = m_x[1] - m_x[0];
    m_c[1] = (m_y[1] - m_y[0]) / m_d[0];

    for (i = 1; i < n - 1; i++) {
      m_d[i] = m_x[i + 1] - m_x[i];
      m_b[i] = 2.0 * (m_d[i - 1] + m_d[i]);
      m_c[i + 1] = (m_y[i + 1] - m_y[i]) / m_d[i];
      m_c[i] = m_c[i + 1] - m_c[i];
    }

    m_b[0] = -m_d[0];
    m_b[n - 1] = -m_d[n - 2];

    m_c[0] = m_c[2] / (m_x[3] - m_x[1]) - m_c[1] / (m_x[2] - m_x[0]);
    m_c[0] = m_c[0] * m_d[0] * m_d[0] / (m_x[3] - m_x[0]);
    m_c[n - 1] = m_c[n - 2] / (m_x[n - 1] - m_x[n - 3]) - m_c[n - 3] / (m_x[n - 2] - m_x[n - 4]);
    m_c[n - 1] = -m_c[n - 1] * m_d[n - 2] * m_d[n - 2] / (m_x[n - 1] - m_x[n - 4]);

    for (i = 1; i < n; i++) {
      double t = m_d[i - 1] / m_b[i - 1];
      m_b[i] = m_b[i] - t * m_d[i - 1];
      m_c[i] = m_c[i] - t * m_c[i - 1];
    }

    m_c[n - 1] = m_c[n - 1] / m_b[n - 1];

    for (i = n - 2; i >= 0; i--) {
      m_c[i] = (m_c[i] - m_d[i] * m_c[i + 1]) / m_b[i];
    }

    m_b[n - 1] = (m_y[n - 1] - m_y[n - 2]) / m_d[n - 2] + m_d[n - 2] * (m_c[n - 2] + 2.0 * m_c[n - 1]);

    for (i = 0; i < n - 1; i++) {
      m_b[i] = (m_y[i + 1] - m_y[i]) / m_d[i] - m_d[i] * (m_c[i + 1] + 2.0 * m_c[i]);
      m_d[i] = (m_c[i + 1] - m_c[i]) / m_d[i];
      m_c[i] = 3.0 * m_c[i];
    }

    m_c[n - 1] = 3.0 * m_c[n - 1];
    m_d[n - 1] = m_d[n - 2];

  }

